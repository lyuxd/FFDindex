package Util;

public class Branch {
	private boolean derection;
	private String edgeLable;
	private String vertexLable;
	
	
	public boolean getDerection() {
		return derection;
	}
	public void setDerection(boolean derection) {
		this.derection = derection;
	}
	public String getEdgeLable() {
		return edgeLable;
	}
	public void setEdgeLable(String edgeLable) {
		this.edgeLable = edgeLable;
	}
	public String getVertexLable() {
		return vertexLable;
	}
	public void setVertexLable(String vertexLable) {
		this.vertexLable = vertexLable;
	}
	
}
